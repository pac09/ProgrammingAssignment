		[LocCode] NVARCHAR(10) NULL,
		[Powr] INT NULL,
		[AziMuth] INT NULL,
		[Slew] INT NULL,
		[AntCode] INT NULL,
		[Days] BIGINT NULL,
		[Sdate] NVARCHAR(25) NULL,
		[Edate] NVARCHAR(25) NULL,
		[Mod] NVARCHAR(3) NULL,
		[Afrq] BIGINT NULL,
		[LanguageCode] NVARCHAR(10) NULL,
		[AdminCode] NVARCHAR(3) NULL,
		[BroadcasterCode] NVARCHAR(3) NULL,
		[FmoCode] NVARCHAR(3) NULL,

		CONSTRAINT PK_TBL_HF_SCHEDULE PRIMARY KEY CLUSTERED ([TblHfScheduleId])
