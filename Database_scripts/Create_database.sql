CREATE TABLE [dbo].[TBL_LANGUAGE](
	    [TblLanguageId] INT IDENTITY(1,1) NOT NULL,
		[Code] NVARCHAR(3) NULL,
		[Language] NVARCHAR(75) NULL,

		CONSTRAINT PK_TBL_LANGUAGE PRIMARY KEY CLUSTERED ([TblLanguageId])
