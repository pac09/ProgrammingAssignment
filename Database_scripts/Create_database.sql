CREATE DATABASE [RadioBroadcasts];



CREATE TABLE [dbo].[TBL_ADMIN](
	    [TblAdminId] INT IDENTITY(1,1) NOT NULL,
		[Code] NVARCHAR(3) NULL,
		[AdminName] NVARCHAR(100) NULL,

		CONSTRAINT PK_TBL_ADMIN PRIMARY KEY CLUSTERED ([TblAdminId])
);

CREATE TABLE [dbo].[TBL_ANT](
	    [TblAntId] INT IDENTITY(1,1) NOT NULL,
		[Code] INT NULL,
		[Ant] NVARCHAR(75) NULL,

		CONSTRAINT PK_TBL_ANT PRIMARY KEY CLUSTERED ([TblAntId])
);

CREATE TABLE [dbo].[TBL_BROADCASTER](
	    [TblBroadcasterId] INT IDENTITY(1,1) NOT NULL,
		[Code] NVARCHAR(3) NULL,
		[Broadcaster] NVARCHAR(140) NULL,

		CONSTRAINT PK_TBL_BROADCASTER PRIMARY KEY CLUSTERED ([TblBroadcasterId])
);

